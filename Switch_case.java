public class Switch_case {
    public static void main(String args[])
    {
        int season=4;
        switch(season)
        {
            case 1:
                System.out.println("Season is Spring");
                break;
            case 2:
                System.out.println("Season is Summer");
                break;
            case 3:
                System.out.println("Season is Fall");
                break;
            case 4:
                System.out.println("Season is Winter");
                break;
        }
    }
}
